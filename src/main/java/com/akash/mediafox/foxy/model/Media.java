package com.akash.mediafox.foxy.model;



public class Media {

    private String campaignId;

    private String instagramMediaId;

    private String twitterMediaId;

    private String youtubeMediaId;

    public String getCampaignId() {
        return campaignId;
    }

    public void setCampaignId(String campaignId) {
        this.campaignId = campaignId;
    }

    public String getInstagramMediaId() {
        return instagramMediaId;
    }

    public void setInstagramMediaId(String instagramMediaId) {
        this.instagramMediaId = instagramMediaId;
    }

    public String getTwitterMediaId() {
        return twitterMediaId;
    }

    public void setTwitterMediaId(String twitterMediaId) {
        this.twitterMediaId = twitterMediaId;
    }

    public String getYoutubeMediaId() {
        return youtubeMediaId;
    }

    public void setYoutubeMediaId(String youtubeMediaId) {
        this.youtubeMediaId = youtubeMediaId;
    }
}
